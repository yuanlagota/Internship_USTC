!===============================================================================
!
! MACHCON IS A SET OF THREE FORTRAN FUNCTIONS THAT PROVIDE MACHINE
! DEPENDENT INFORMATION.
!
!      THE FORM OF THESE FUNCTIONS ARE
!         R = R1MACH(K)
!         I = I1MACH(K)
!         D = D1MACH(K)
! WHERE
!         R    IS A REAL (SINGLE PRECISION) NUMBER
!         I    IS AN INTEGER
!         D    IS A DOUBLE PRECISION NUMBER
! AND
!         K    IS AN INTEGER
!
! BY USING THESE FUNCTIONS IN YOUR FORTRAN PROGRAMS, LARGE NUMBERS OF
! PROGRAMS CAN BE MOVED FROM ONE MACHINE TO ANOTHER BY SIMPLY CHANGING
! THESE THREE FUNCTIONS.
!
!===============================================================================
